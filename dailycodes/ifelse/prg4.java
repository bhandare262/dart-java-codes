class ifelsedemo{
	public static void main(String[] args){
		char ch= 97;
		  System.out.println(ch);   // a=97
		  if (ch=='a'){
			  ch+=3;  //100=d
			    System.out.println(ch--); //100=d & ch=99
		  }else{
			    System.out.println(ch++); //99=b & ch=100
		  }
		    System.out.println(ch+=5); //105
	}
}                                                                                                                                                                                                                      
