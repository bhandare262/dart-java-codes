 class ifelsedemo{
	 public static void main(String[] args){
		 char ch= 97;
		   System.out.println(ch=ch+8);   // 105 =h
		   if(ch=='a')  {
			   int x =5;
			     System.out.println(ch-- +x);  //102 =f
		   }else{
			   int x= 7;
			     System.out.println(ch++);  //105
			       System.out.println(x);  //7
		   }
		     System.out.println(ch+=5);  //110 =m
	 }
 }
			 
