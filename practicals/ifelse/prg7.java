class ifelse{
	public static void main(String[] args){
		int num1= 5;
		int num2 = 9;
		if(num1>num2){
			                 System.out.println(num1 + "maximum than " + num2);
		}else if(num1<num2){
			                 System.out.println(num1 + "minimum than "+ num2);
		}else {
			                 System.out.println(num1 + "equal to" + num2);
		}
	}
}

































